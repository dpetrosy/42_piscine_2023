int	ft_str_is_alpha(char *str)
{
	char	ch;

	if (!str || *str == '\0')
		return (1);
	ch = *str;
	while (ch != '\0')
	{
		if (!((ch >= 65 && ch <= 90) || (ch >= 97 && ch <= 122)))
			return (0);
		ch = *(++str);
	}
	return (1);
}
