int	ft_str_is_lowercase(char *str)
{
	char	ch;

	if (!str || *str == '\0')
		return (1);
	ch = *str;
	while (ch != '\0')
	{
		if (!(ch >= 'a' && ch <= 'z'))
			return (0);
		ch = *(++str);
	}
	return (1);
}
