int	ft_str_is_numeric(char *str)
{
	char	ch;

	if (!str || *str == '\0')
		return (1);
	ch = *str;
	while (ch != '\0')
	{
		if (!(ch >= 48 && ch <= 57))
			return (0);
		ch = *(++str);
	}
	return (1);
}
